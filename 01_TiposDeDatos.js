/**
 * Tipos de datos
 */

// numer0--number
console.log(27);
console.log(27.56);

// texto--string
console.log("Ronald Conde");
console.log('Fernando Flores');
console.log(`Hola`);

// Logicos--booleans(True/False)
console.log(true);
console.log(false);

// indefinido--undefined
console.log(undefined);

// null
console.log(null);

/**
 * Tipos de datos estructurales
 */

// object
console.log({ nombre:'Ronald', edad: 27, fechaDeNacimiento: null, esAlto: false }
);

// dato tipo lista-- array
console.log(['Mexico', 'Venezuela', 'Ecuador',]);

console.log([21, true,{nombre: 'Gabriel'}, [1,2,3,4]]);

